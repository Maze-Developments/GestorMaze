    private void jPro_tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPro_tabelaMouseClicked
        // TODO add your handling code here:
        jBtn_editar.setEnabled(true);
        jBPro_salvar.setEnabled(false);
        int linha = jPro_tabela.getSelectedRow();

        // Pega valores da tabela na linha selcionada
        int codigoProduto = (int) jPro_tabela.getValueAt(linha, 0);
        String nomeProduto = (String) jPro_tabela.getValueAt(linha, 1);
        String categoProduto = (String) jPro_tabela.getValueAt(linha, 2);
        int quantidadeProduto = (int) jPro_tabela.getValueAt(linha, 3);
        double precoProduto = (double) jPro_tabela.getValueAt(linha, 4);

        //diciona os valores nos campos
        jPro_codigo.setText(Integer.toString(codigoProduto));
        jPro_nome.setText(nomeProduto);
        jPro_categoria.setSelectedItem(categoProduto);
        jPro_quantidade.setText(Integer.toString(quantidadeProduto));
        jPro_preco.setText(Double.toString(precoProduto));

    }//GEN-LAST:event_jPro_tabelaMouseClicked

    private void jBPro_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBPro_pesquisarActionPerformed
        // TODO add your handling code here:
        String nome = jPro_pesquisar.getText().trim() + "%";
        ProdutoDAO dao = new ProdutoDAO();
        List<Produto> lista = dao.pesquisarProNome(nome);
        DefaultTableModel dados = (DefaultTableModel) jPro_tabela.getModel();
        dados.setNumRows(0);

        for (Produto p : lista) {
            dados.addRow(new Object[]{
                p.getId_produto(),
                p.getPro_nome(),
                p.getPro_categoria(),
                p.getPro_quantidade(),
                p.getPro_preco()
            });
        }

    }//GEN-LAST:event_jBPro_pesquisarActionPerformed

