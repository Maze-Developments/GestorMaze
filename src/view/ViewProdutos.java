    public void listar() {
        ProdutoDAO dao = new ProdutoDAO();
        List<Produto> lista = dao.listarProdutos();
        DefaultTableModel dados = (DefaultTableModel) jPro_tabela.getModel();
        dados.setNumRows(0);

        for (Produto p : lista) {
            dados.addRow(new Object[]{
                p.getId_produto(),
                p.getPro_nome(),
                p.getPro_categoria(),
                p.getPro_quantidade(),
                p.getPro_preco(),});
        }
    }

    public void listarCategorias() {
        CategoriaDAO dao = new CategoriaDAO();
        List<Categoria> lista = dao.listarCategorias();

        jPro_categoria.removeAllItems();
        for (Categoria cat : lista) {
            jPro_categoria.addItem(cat.toString());
        }
    }

